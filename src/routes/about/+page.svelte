<h1>About me</h1>

<p>
  A vampire is a mythical creature that subsists by feeding on the vital essence
  (generally in the form of blood) of the living.
</p>

<p>
  In European folklore, vampires are undead creatures that often visited loved
  ones and caused mischief or deaths in the neighbourhoods which they inhabited
  while they were alive.
</p>

<p>
  They wore shrouds and were often described as bloated and of ruddy or dark
  countenance, markedly different from today's gaunt, pale vampire which dates
  from the early 19th century.
</p>
<p>Vampiric entities have been recorded in cultures around the world;</p>
<p>
  the term vampire was popularized in Western Europe after reports of an
  18th-century mass hysteria of a pre-existing folk belief in Southeastern and
  Eastern Europe that in some cases resulted in corpses being staked and people
  being accused of vampirism.
</p>
<p>
  Local variants in Southeastern Europe were also known by different names, such
  as shtriga in Albania, vrykolakas in Greece and strigoi in Romania.
</p>

